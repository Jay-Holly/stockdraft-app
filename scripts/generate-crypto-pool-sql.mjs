/**
 * Fetches top cryptocurrencies from CoinGecko and writes
 * supabase/migrations/030_crypto_pool.sql
 *
 * Filters: top 50 by market cap, excluding stablecoins, gold-pegged,
 * wrapped/duplicate tokens, and tokenized RWAs. No per-coin price floor.
 */
import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const TARGET_SIZE = 50;
const MARKETS_PAGES = [1, 2];

const EXCLUDED_SYMBOLS = new Set([
  "USDT", "USDC", "DAI", "BUSD", "TUSD", "USDP", "USDD", "FDUSD", "PYUSD",
  "FRAX", "LUSD", "GUSD", "SUSD", "USDE", "USD0", "USDS", "USDL", "USDY",
  "EURC", "EURT", "PAXG", "XAUT", "XAUt", "TGBP", "AUSD", "CUSD", "USDB",
  "UST", "USTC", "MIM", "CRVUSD", "GHO", "MKUSD", "USTB", "OUSG", "BCAP",
  "FIUSD", "PGOLD", "XAUM", "TSLAX", "EARNETH", "STRCX", "ACRED", "KOGE",
  "EXOD", "SN64", "SN4", "VCNT", "GRX", "QFI", "DIEM", "ULTIMA", "NXM",
  "WBTC", "WETH", "STETH", "WSTETH", "CBETH", "RETH", "WBETH", "WEETH",
  "BETH", "METH", "WBNB", "WSOL", "USD1", "USDF", "BFUSD", "USYC", "BUIDL",
  "FIGR_HELOC",
]);

const EXCLUDED_IDS = new Set([
  "tether", "usd-coin", "dai", "binance-usd", "true-usd", "pax-gold",
  "tether-gold", "paypal-usd", "first-digital-usd", "ripple-usd", "usdd",
  "frax", "liquity-usd", "gemini-dollar", "stasis-eurs", "euro-coin",
  "tether-eurt", "global-dollar", "usds", "usdb", "usde", "crvusd", "gho",
  "magic-internet-money", "terrausd", "terrausd-wormhole", "usd0-liquid-bond",
  "mountain-protocol-usdm", "ondo-us-dollar-yield", "ousg", "superstate-uscc",
  "superstate-short-duration-us-government-securities-fund-ustb",
  "blockchain-capital", "kinesis-gold", "kinesis-silver",
  "wrapped-bitcoin", "weth", "staked-ether", "lido-staked-ether", "wrapped-steth",
  "coinbase-wrapped-staked-eth", "rocket-pool-eth", "mantle-staked-ether",
  "wrapped-beacon-eth", "wrapped-eeth", "binance-eth", "wrapped-solana",
  "wrapped-bnb-wormhole", "blackrock-usd-institutional-digital-liquidity-fund",
  "figure-heloc", "usd1-wormhole", "usdf", "bfusd",
]);

const EXCLUDED_NAME_PATTERNS = [
  /pax gold/i,
  /tether gold/i,
  /gold token/i,
  /gold-pegged/i,
  /wrapped gold/i,
  /treasury/i,
  /t-bill/i,
  /government securities/i,
  /money market/i,
  /tokenized stock/i,
  /tokenized equity/i,
  /ondo /i,
  /superstate/i,
  /^wrapped /i,
  / liquid staking/i,
  / staked ether/i,
];

function escapeSql(value) {
  return String(value).replace(/'/g, "''");
}

function isExcluded(coin) {
  const symbol = coin.symbol.toUpperCase();
  const id = coin.id.toLowerCase();
  const name = coin.name.toLowerCase();

  if (EXCLUDED_SYMBOLS.has(symbol)) return true;
  if (EXCLUDED_IDS.has(id)) return true;
  if (EXCLUDED_NAME_PATTERNS.some((pattern) => pattern.test(name))) return true;
  if (/\b(stablecoin|pegged usd|usd stable)\b/i.test(name)) return true;
  return false;
}

async function fetchMarketsPage(page) {
  const url = `https://api.coingecko.com/api/v3/coins/markets?vs_currency=usd&order=market_cap_desc&per_page=250&page=${page}&sparkline=false`;
  const res = await fetch(url, { headers: { Accept: "application/json" } });
  if (!res.ok) {
    throw new Error(`CoinGecko markets fetch failed (page ${page}): HTTP ${res.status}`);
  }
  return res.json();
}

async function main() {
  const markets = [];
  for (const page of MARKETS_PAGES) {
    const batch = await fetchMarketsPage(page);
    markets.push(...batch);
    if (page < MARKETS_PAGES.length) {
      await new Promise((resolve) => setTimeout(resolve, 1200));
    }
  }

  const selected = [];

  for (const coin of markets) {
    if (selected.length >= TARGET_SIZE) break;
    if (!coin.symbol || !coin.id || !coin.name) continue;
    if (isExcluded(coin)) continue;
    if (selected.some((entry) => entry.symbol === coin.symbol.toUpperCase())) continue;

    selected.push({
      symbol: coin.symbol.toUpperCase(),
      name: coin.name,
      coingeckoId: coin.id,
      marketCapRank: coin.market_cap_rank ?? selected.length + 1,
      referencePriceUsd: coin.current_price ?? null,
    });
  }

  if (selected.length < TARGET_SIZE) {
    console.warn(
      `Warning: only ${selected.length} coins passed filters (target ${TARGET_SIZE}).`
    );
  }

  const values = selected
    .map(
      (coin) =>
        `  ('${escapeSql(coin.symbol)}', '${escapeSql(coin.name)}', '${escapeSql(coin.coingeckoId)}', ${coin.marketCapRank}, ${coin.referencePriceUsd ?? "null"})`
    )
    .join(",\n");

  const sql = `-- Phase: Top-50 crypto draft pool (CoinGecko market cap rank)
-- Generated by scripts/generate-crypto-pool-sql.mjs on ${new Date().toISOString()}
-- Re-run: npm run generate:crypto-pool

create table if not exists public.crypto_pool (
  symbol text primary key,
  name text not null,
  coingecko_id text not null unique,
  market_cap_rank int not null check (market_cap_rank >= 1),
  reference_price_usd numeric,
  updated_at timestamptz not null default now()
);

create index if not exists crypto_pool_rank_idx on public.crypto_pool (market_cap_rank);

alter table public.crypto_pool enable row level security;

drop policy if exists "crypto_pool_read_authenticated" on public.crypto_pool;
create policy "crypto_pool_read_authenticated"
  on public.crypto_pool
  for select
  to authenticated
  using (true);

drop policy if exists "crypto_pool_service_write" on public.crypto_pool;
create policy "crypto_pool_service_write"
  on public.crypto_pool
  for all
  to service_role
  using (true)
  with check (true);

truncate public.crypto_pool;

insert into public.crypto_pool (symbol, name, coingecko_id, market_cap_rank, reference_price_usd)
values
${values}
on conflict (symbol) do update
  set name = excluded.name,
      coingecko_id = excluded.coingecko_id,
      market_cap_rank = excluded.market_cap_rank,
      reference_price_usd = excluded.reference_price_usd,
      updated_at = now();
`;

  const root = join(dirname(fileURLToPath(import.meta.url)), "..");
  const outPath = join(root, "supabase/migrations/030_crypto_pool.sql");
  writeFileSync(outPath, sql, "utf8");

  console.log(`Wrote ${selected.length} coins to ${outPath}`);
  console.log(selected.map((c) => c.symbol).join(", "));
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
